def detect_category(text: str) -> str:
    """
    Определяет категорию квеста по текстовому описанию.

    Args:
        text: Текст описания квеста

    Returns:
        Название категории (str)
    """
    if not text:
        return "Other"

    text_lower = text.lower()

    # Подсчитываем совпадения для каждой категории
    scores = {}
    for category, keywords in CATEGORIES.items():
        if category == "Other":  # Other оставляем на конец
            continue

        # Ищем ключевые слова
        score = 0
        for keyword in keywords:
            if keyword in text_lower:
                score += 1

        if score > 0:
            scores[category] = score

    # Возвращаем категорию с максимумом совпадений
    if scores:
        best_category = max(scores, key=scores.get)
        return best_category

    return "Other"


def get_category_emoji(category: str) -> str:
    """Возвращает эмодзи для категории"""
    emoji_map = {
        "1С": "📊",
        "Windows": "🪟",
        "Office": "📄",
        "Network": "🌐",
        "AutoCAD": "📐",
        "Printer": "🖨️",
        "Mobile": "📱",
        "Hardware": "🔧",
        "Other": "❓"
    }
    return emoji_map.get(category, "❓")


def format_category_tag(category: str) -> str:
    """Форматирует тег категории для вывода"""
    emoji = get_category_emoji(category)
    return f"{emoji} {category}"
